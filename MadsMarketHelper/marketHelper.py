def add_loja(nome: str, especialidade: str, localizacao: str):
    global contador_lojas
    erros = []

#Verificação de campos totais
    if not nome or not especialidade or not localizacao:
        erros.append("Erro: Todos os campos são de preenchimento obrigatório.")

#Verificações do Nome da Loja
    if not isinstance(nome, str):
        erros.append("Erro: O nome da loja deve ser texto.")
    elif not re.fullmatch(r"[A-Za-zÀ-ÖØ-öø-ÿ0-9\s\&]+", nome):
        erros.append("Erro: O nome da loja contém caracteres inválidos.")

#Verificações da Localização
    if not isinstance(localizacao, str):
        erros.append("Erro: A localização deve ser texto.")
    elif not re.fullmatch(r"[A-Za-zÀ-ÖØ-öø-ÿ\s\-]+", localizacao):
        erros.append("Erro: A localização contém caracteres inválidos (ex: números).")

#Verificações da Especialidade
    if isinstance(especialidade, str):
        especialidade_formatada = especialidade.strip().capitalize()
        if especialidade_formatada not in categoriasLojas:
            erros.append(f"Erro: Especialidade inválida. Escolha uma das seguintes: {', '.join(categoriasLojas)}.")
    else:
        erros.append("Erro: A especialidade deve ser texto.")

#Só fazemos a procura se o nome e localização forem efetivamente texto válido
    if isinstance(nome, str) and isinstance(localizacao, str) and nome and localizacao:
        for loja_existente in lojas.values():
            if loja_existente["nome"].lower() == nome.strip().lower() and loja_existente["localizacao"].lower() == localizacao.strip().lower():
                erros.append(f"Erro: A loja '{nome.strip()}' em '{localizacao.strip()}' já se encontra registada!")
