def consultar_estatisticas():
    print("\n" + "="*45)
    print("   PAINEL DE ESTATÍSTICAS GLOBAIS   ")
    print("="*45)

#Exibição dos Totais Base
    print(f"Total de Utilizadores: {len(utilizadores)}")
    print(f"Total de Lojas Registadas: {len(lojas)}")
    print(f"Total de Compras Realizadas: {len(compras)}")

#Verificação de dados para métricas avançadas
    if not compras:
        print("\nAviso: Ainda não existem compras registadas para gerar métricas agregadas.")
        return

    contagem_lojas = {}
    contagem_produtos = {}
    contagem_users = {}
    gastos_users = {}
    total_gasto = 0.0

#Processamento dos dados de todas as compras
    for dados in compras.values():
        # Contagem para a Loja mais popular
        lid = dados['id_loja']
        contagem_lojas[lid] = contagem_lojas.get(lid, 0) + 1

        # Contagem para o Produto mais vendido
        prod = dados['produto']
        contagem_produtos[prod] = contagem_produtos.get(prod, 0) + 1

        # Contagem e Gastos para o Utilizador
        nif = dados.get('nif_utilizador')
        if nif:
            contagem_users[nif] = contagem_users.get(nif, 0) + 1
            gastos_users[nif] = gastos_users.get(nif, 0.0) + dados['preco']

        # Soma do volume financeiro
        total_gasto += dados['preco']

#Cálculo dos "Vencedores"
    loja_top_id = max(contagem_lojas, key=contagem_lojas.get)
    loja_top = lojas[loja_top_id]
    vendas_loja_top = contagem_lojas[loja_top_id]

    produto_top = max(contagem_produtos, key=contagem_produtos.get)
    vendas_produto_top = contagem_produtos[produto_top]

    # Vencedores dos Utilizadores
    if contagem_users:
        nif_top_compras = max(contagem_users, key=contagem_users.get)
        nif_top_gastos = max(gastos_users, key=gastos_users.get)

        nome_top_compras = utilizadores.get(nif_top_compras, {}).get("nome", "Desconhecido")
        nome_top_gastos = utilizadores.get(nif_top_gastos, {}).get("nome", "Desconhecido")

        vendas_user_top = contagem_users[nif_top_compras]
