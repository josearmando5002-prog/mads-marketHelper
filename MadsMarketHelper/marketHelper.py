def listar_dados(entidade: str, ordem: str = "asc", filtro_tipo: str = "", filtro_valor: str = ""):
    erros = []
    entidades_validas = ["utilizadores", "lojas", "compras"]

#Verificação da entidade solicitada
    if not isinstance(entidade, str):
        erros.append("Erro: A entidade deve ser texto.")
    elif entidade.strip().lower() not in entidades_validas:
        erros.append(f"Erro: Entidade inválida. Escolha entre: {', '.join(entidades_validas)}.")

#Verificação e conversão da ordem (asc/desc para boolean)
    ordem_descendente = False
    if isinstance(ordem, str):
        if ordem.strip().lower() == "desc":
            ordem_descendente = True
        elif ordem.strip().lower() != "asc":
            erros.append("Erro: A ordem de listagem deve ser 'asc' ou 'desc'.")
    else:
        erros.append("Erro: A ordem deve ser texto ('asc' ou 'desc').")

#Lista de erros, se existirem
    if erros:
        for erro in erros:
            print(f"- {erro}")
        return

    entidade_formatada = entidade.strip().lower()

#Listar Utilizadores
    if entidade_formatada == "utilizadores":
        if not utilizadores:
            print("Aviso: Ainda não existem utilizadores registados.")
            return

        print(f"\n--- LISTA DE UTILIZADORES ---")
        itens = sorted(utilizadores.items(), reverse=ordem_descendente)
        resultados = 0

        for nif, dados in itens:
            if filtro_tipo.lower() == "sexo" and dados['sexo'] != filtro_valor.strip().upper():
                continue

            print(f"NIF: {nif} | Nome: {dados['nome']} | Idade: {dados['idade']} anos | Sexo: {dados['sexo']}")
            resultados += 1

        if resultados == 0:
            print("Nenhum utilizador encontrado com esses critérios.")

#Listar Lojas
    elif entidade_formatada == "lojas":
        if not lojas:
            print("Aviso: Ainda não existem lojas registadas.")
            return

        print(f"\n--- LISTA DE LOJAS ---")
        itens = sorted(lojas.items(), reverse=ordem_descendente)
        resultados = 0

        for id_loja, dados in itens:
            if filtro_tipo.lower() == "especialidade" and dados['especialidade'].lower() != filtro_valor.strip().lower():
                continue

            print(f"ID: {id_loja} | Nome: {dados['nome']} | Especialidade: {dados['especialidade']} | Localização: {dados['localizacao']}")
            resultados += 1

        if resultados == 0:
            print("Nenhuma loja encontrada com esses critérios.")

#Listar Compras
    elif entidade_formatada == "compras":
        if not compras:
            print("Aviso: Ainda não existem compras registadas.")
            return

        print(f"\n--- LISTA DE COMPRAS ---")
        itens = sorted(compras.items(), reverse=ordem_descendente)
        resultados = 0

        for id_compra, dados in itens:
            if filtro_tipo.lower() == "produto" and dados['produto'] != filtro_valor.strip().lower():
                continue
            elif filtro_tipo.lower() == "loja" and str(dados['id_loja']) != str(filtro_valor).strip():
                continue
            elif filtro_tipo.lower() == "pagamento" and dados['tipo_pagam'].lower() != filtro_valor.strip().lower():
                continue
            elif filtro_tipo.lower() == "com_pagamento_registado" and dados['tipo_pagam'] == "Não especificado":
                continue
            elif filtro_tipo.lower() == "nif" and str(dados.get('nif_utilizador', '')) != str(filtro_valor).strip():
                continue

            nome_loja = lojas[dados['id_loja']]["nome"]
            # Captura o nome do utilizador para mostrar na listagem
            nif_comp = dados.get('nif_utilizador', '')
            nome_user = utilizadores[nif_comp]["nome"] if nif_comp in utilizadores else "Desconhecido"

            produto_formatado = dados['produto'].capitalize()

            # Print atualizado com o nome do comprador
            print(f"ID: {id_compra} | Produto: {produto_formatado} | Preço: {dados['preco']:.2f}€ | Comprador: {nome_user} | Loja: {nome_loja} | Data: {dados['data']} | Pagamento: {dados['tipo_pagam']}")
            resultados += 1

        if resultados == 0:
            print("Nenhuma compra encontrada com esses critérios.")

