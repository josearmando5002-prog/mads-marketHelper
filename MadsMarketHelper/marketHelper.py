contador_compras = 1

def add_compra(produto: str, preco: float, id_loja: int, data_compra: str, nif_utilizador: str, tipo_pagam: str = ""):
    global contador_compras
    erros = []

#Verificação de campos totais (Sem tipo de pagamento pois isso é opcional)
    if not produto or not preco or not id_loja or not data_compra or not nif_utilizador:
        erros.append("Erro: Produto, Preço, ID da Loja, Data e NIF do Utilizador são obrigatórios.")

#Verificações do Produto
    if not isinstance(produto, str):
        erros.append("Erro: O nome do produto deve ser texto.")
    elif not re.fullmatch(r"[A-Za-zÀ-ÖØ-öø-ÿ0-9\s\-]+", produto):
        erros.append("Erro: O nome do produto contém caracteres inválidos.")

#Verificações do Preço
    if not isinstance(preco, (int, float)):
        erros.append("Erro: O preço deve ser um valor numérico (inteiro ou decimal).")
    elif preco <= 0:
        erros.append("Erro: O preço tem de ser estritamente superior a 0.")

#Verificações do ID da Loja
    if isinstance(id_loja, int):
        if id_loja not in lojas:
            erros.append(f"Erro: Não existe nenhuma loja registada com o ID {id_loja}. Registe a loja primeiro.")
    else:
        erros.append("Erro: O ID da loja deve ser um número inteiro.")

# Verificação do NIF
    if isinstance(nif_utilizador, str):
        if nif_utilizador not in utilizadores:
            erros.append(f"Erro: Não existe nenhum utilizador com o NIF {nif_utilizador}. Registe o utilizador primeiro.")
    else:
        erros.append("Erro: O NIF do utilizador deve ser texto.")

#Verificações da Data da Compra
    if isinstance(data_compra, str):
        if not re.match(r"^\d{2}-\d{2}-\d{4}$", data_compra):
            erros.append("Erro: A data da compra deve estar no formato DD-MM-YYYY.")
        else:
            try:
                data_convertida = datetime.strptime(data_compra, "%d-%m-%Y").date()
                hoje = datetime.now().date()
                if data_convertida > hoje:
                    erros.append("Erro: A data da compra não pode ser no futuro.")
            except ValueError:
                erros.append("Erro: Data de compra inválida.")
    else:
        erros.append("Erro: A data da compra deve ser texto no formato DD-MM-YYYY.")

#Verificações do Tipo de Pagamento
    if tipo_pagam:
        if not isinstance(tipo_pagam, str):
            erros.append("Erro: O tipo de pagamento deve ser texto ('N' ou 'M').")
        elif tipo_pagam.strip().upper() not in ["N", "M"]:
            erros.append("Erro: Tipo de pagamento inválido. Use 'N' para Numerário ou 'M' para Multibanco.")

#Lista de erros, se existirem
    if erros:
        for erro in erros:
            print(f"- {erro}")
        return

#Formatação do tipo de pagamento
    if tipo_pagam.strip().upper() == "N":
        pagamento_final = "Numerário"
    elif tipo_pagam.strip().upper() == "M":
        pagamento_final = "Multibanco"
    else:
        pagamento_final = "Não especificado"

#Registro da Compra após todas as verificações
    compras[contador_compras] = {
        "nif_utilizador": nif_utilizador.strip(),  # <-- GUARDADO AQUI
        "produto": produto.strip().lower(),
        "preco": float(preco),
        "id_loja": id_loja,
        "data": data_compra.strip(),
        "tipo_pagam": pagamento_final
    }

    nome_loja = lojas[id_loja]["nome"]
    nome_user = utilizadores[nif_utilizador]["nome"]
    print(f"Sucesso: Compra de '{produto.strip().capitalize()}' registada por {nome_user} na loja '{nome_loja}'! (ID: {contador_compras})")

    contador_compras += 1

