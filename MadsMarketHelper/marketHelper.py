def exportar_para_json(nome_ficheiro: str = "dados_plataforma.json", entidade: str = "tudo", filtro_tipo: str = "", filtro_valor: str = ""):
    erros = []

#Verificação do nome do ficheiro
    if not isinstance(nome_ficheiro, str):
        erros.append("Erro: O nome do ficheiro deve ser texto.")
    elif not nome_ficheiro.endswith(".json"):
        erros.append("Erro: O nome do ficheiro tem de terminar obrigatoriamente em '.json'.")

#Lista de erros, se existirem
    if erros:
        for erro in erros:
            print(f"- {erro}")
        return

    dados_para_exportar = {}
    entidade_formatada = entidade.strip().lower()

#Cenário 1: Exportação Total
    if entidade_formatada == "tudo":
        dados_para_exportar = {
            "utilizadores": utilizadores,
            "categoriasLojas": categoriasLojas,
            "lojas": lojas,
            "compras": compras
        }
        mensagem = "Todos os dados da plataforma (Exportação Integral)"

#Cenário 2: Exportar Apenas Compras Filtradas
    elif entidade_formatada == "compras":
        compras_filtradas = {}
        for id_compra, dados in compras.items():
            if filtro_tipo.lower() == "produto" and dados['produto'] != filtro_valor.strip().lower():
                continue
            elif filtro_tipo.lower() == "loja" and str(dados['id_loja']) != str(filtro_valor).strip():
                continue
            elif filtro_tipo.lower() == "pagamento" and dados['tipo_pagam'].lower() != filtro_valor.strip().lower():
                continue

            compras_filtradas[id_compra] = dados

        dados_para_exportar = {"compras_filtradas": compras_filtradas}
        mensagem = f"As compras filtradas ({filtro_tipo}={filtro_valor})"

        if not compras_filtradas:
            print("Aviso: Não existem compras com esses filtros para exportar. Ficheiro cancelado.")
            return

#Cenário Extra: Se o utilizador inventar uma entidade
    else:
        print("Erro: A entidade para exportar deve ser 'tudo' ou 'compras'.")
        return

#Tentativa de criação e escrita do ficheiro
    try:
        with open(nome_ficheiro, 'w', encoding='utf-8') as ficheiro:
            json.dump(dados_para_exportar, ficheiro, ensure_ascii=False, indent=4)

        print(f"Sucesso: {mensagem} foram exportados para o ficheiro '{nome_ficheiro}'.")

    except Exception as e:
        print(f"Erro crítico do sistema ao tentar gravar o ficheiro: {e}")

"""# Historico de preços e gráficos"""

def gerar_grafico_evolucao(produto: str, data_inicio: str = "", data_fim: str = ""):
    erros = []
    obj_inicio = None
    obj_fim = None

    if not isinstance(produto, str):
        erros.append("Erro: O nome do produto deve ser texto.")
    elif not produto.strip():
        erros.append("Erro: O nome do produto não pode estar vazio.")

    if data_inicio:
        if not isinstance(data_inicio, str) or not re.match(r"^\d{2}-\d{2}-\d{4}$", data_inicio):
            erros.append("Erro: A data de início deve ser texto no formato DD-MM-YYYY.")
        else:
            try:
                obj_inicio = datetime.strptime(data_inicio, "%d-%m-%Y").date()
            except ValueError:
                erros.append("Erro: Data de início inválida.")

    if data_fim:
        if not isinstance(data_fim, str) or not re.match(r"^\d{2}-\d{2}-\d{4}$", data_fim):
            erros.append("Erro: A data de fim deve ser texto no formato DD-MM-YYYY.")
        else:
            try:
                obj_fim = datetime.strptime(data_fim, "%d-%m-%Y").date()
            except ValueError:
                erros.append("Erro: Data de fim inválida.")

    if obj_inicio and obj_fim and obj_inicio > obj_fim:
        erros.append("Erro: A data de início não pode ser posterior à data de fim.")

    if erros:
        for erro in erros:
            print(f"- {erro}")
        return

    produto_alvo = produto.strip().lower()
    historico = []

    for cid, dados in compras.items():
        if dados['produto'] == produto_alvo:
            data_obj = datetime.strptime(dados['data'], "%d-%m-%Y").date()

            if obj_inicio and data_obj < obj_inicio:
                continue
            if obj_fim and data_obj > obj_fim:
                continue

            historico.append({"data_obj": data_obj, "data_str": dados['data'], "preco": dados['preco']})

    if not historico:
        if data_inicio or data_fim:
            print(f"Erro: Não existem registos de compras para '{produto_alvo.capitalize()}' no período selecionado.")
        else:
            print(f"Erro: Não existem registos de compras para o produto '{produto_alvo.capitalize()}'.")
        return

    historico.sort(key=lambda x: x["data_obj"])

    datas = [item["data_str"] for item in historico]
    precos = [item["preco"] for item in historico]

    print(f"\n--- ANÁLISE DO PRODUTO: {produto_alvo.upper()} ---")
    if len(precos) > 1:
        preco_inicial = precos[0]
        preco_final = precos[-1]
        variacao = ((preco_final - preco_inicial) / preco_inicial) * 100

        print(f"Preço Inicial ({datas[0]}): {preco_inicial:.2f}€")
        print(f"Preço Atual/Final ({datas[-1]}): {preco_final:.2f}€")

        if variacao > 0:
            print(f"Variação: Subiu {variacao:.2f}% ")
        elif variacao < 0:
            print(f"Variação: Desceu {abs(variacao):.2f}% ")
        else:
            print("Variação: O preço manteve-se inalterado. ")
    else:
        print(f"Apenas um registo encontrado: {precos[0]:.2f}€ no dia {datas[0]}.")
        print("Aviso: É necessário pelo menos mais uma compra para calcular a variação de preço.")

    try:
        plt.figure(figsize=(10, 5))
        plt.plot(datas, precos, marker='o', linestyle='-', color='b', linewidth=2, markersize=8)

        titulo = f"Evolução Temporal do Preço: {produto_alvo.capitalize()}"
        if data_inicio or data_fim:
            titulo += f"\n({data_inicio or 'Início'} até {data_fim or 'Fim'})"

        plt.title(titulo, fontsize=14, fontweight='bold')
        plt.xlabel("Data da Compra", fontsize=12)
        plt.ylabel("Preço (€)", fontsize=12)
        plt.grid(True, linestyle='--', alpha=0.7)
        plt.xticks(rotation=45)
        plt.tight_layout()
        plt.show()

    except Exception as e:
        print(f"Erro ao renderizar o gráfico: {e}")
        print("Certifique-se de que tem a linha 'import matplotlib.pyplot as plt' na sua célula de bibliotecas.")

